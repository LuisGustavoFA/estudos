package sintaxe_variaveis_e_fluxo;

public class TestaPontoFlutuante {
	public static void main(String[] args) {
		double salario = 1132.5;
		System.out.println("O salário é " + salario);
		
		double divisao = salario / 2;
		System.out.println("O salário é vai ser " + divisao);
		
		int outraDivisao = 5 / 2;
		System.out.println(outraDivisao);
		
		double outraTentativa = 5 / 2;
		System.out.println(outraTentativa);
		
		double tentativaCorreta = 5.0 / 2;
		System.out.println(tentativaCorreta);
	}
}
