package sintaxe_variaveis_e_fluxo;

public class TestaCaracteres {

	public static void main(String[] args) {
		char letra = 'A';
		System.out.println(letra);
		
		char valor = 66;
		System.out.println(valor);
		
		valor = (char) (valor + 1);
		System.out.println(valor);
		
		String frase = "bah";
		System.out.println(frase);
		
		int ano = 2023;
		System.out.println(frase + " " + ano);
	}

}
